package geometrie;

public class Point {
	
	private double x;
	private double y;

	//constructeur
	public Point () {}


	//accesseurs
	public void setX(double value) {
		this.x = value;
	}

	public double getX() {
		return this.x;
	}



	public void setY(double value) {
		this.y = value;
	}

	public double getY() {
		return this.y;
	}
	// Fin accesseurs
}
